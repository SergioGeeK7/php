<h4>Arena de Sao Paublo</h4>
<p>Desde su construcción en 1972, el estadio João Cláudio de Vasconcelos Machado, o Machadão, albergó en el barrio Lagoa Nova los mejores partidos de fútbol de la capital del estado de Río Grande del Norte, sobre todo los que implicaban a sus tres equipos principales: el ABC, que jugó en la primera división del campeonato brasileño por última vez en 1985, el Alecrim (1986) y el América (2007). </p>

<img src="img/estadio8.jpg" alt="">
<h4>Estadio Beira-Rio - Porto Alegre</h4>
<p>El Beira-Rio, levantado en un relleno del río Guaíba, uno de los emblemas de Porto Alegre, es el hogar del Sport Club Internacional, que comparte con su acérrimo rival, el Grêmio, el protagonismo del polarizado fútbol de Rio Grande do Sul. Escenario de innumerables y emocionantes clásicos entre ambos clubes y de cuatro finales de la Copa Libertadores de América, el recinto se prepara para su misión más importante: albergar cinco encuentros de la Copa Mundial de la FIFA Brasil 2014™, uno de ellos de cuartos de final del certamen. </p>
<img src="img/estadio9.jpg" alt="">
<h4>Arena Pernambuco - Recife</h4>
<p>Recife, ciudad apasionada por el fútbol y sede de tres venerables clubes en el ámbito nacional como son el Náutico, el Santa Cruz y el Sport Club, ya albergó un encuentro de la Copa Mundial de la FIFA Brasil 1950 (Chile 5-2 Estados Unidos, disputado en la Isla del Retiro) y ganará un nuevo estadio para acoger cinco partidos de Brasil 2014™. </p>
<img src="img/estadio10.jpg" alt="">
<h4>Estadio Maracaná - Río de Janeiro</h4>
<p>Construido para la Copa Mundial de la FIFA 1950™ e inmortalizado como escenario de la inolvidable final entre Brasil y Uruguay, uno de los capítulos más destacados de la historia del torneo, el Estadio Periodista Mário Filho, de Río de Janeiro, recupera el protagonismo con motivo de Brasil 2014. El Maracaná es el recinto que mayor número de partidos albergará durante el certamen, siete en total, entre ellos la gran final del día 13 de julio. </p>
