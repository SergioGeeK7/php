<h4>Estadio Mineirão, Belo Horizonte</h4>	
<p>	Este recinto, uno de los templos del fútbol brasileño y casa de los multicampeones nacionales Atlético Mineiro y Cruzeiro, tiene capacidad para 66.805 espectadores y será completamente reformado para alojar seis partidos de la Copa Mundial de la FIFA Brasil 2014™, incluyendo un encuentro de semifinales. . </p>

<img src="img/estadio2.jpg"alt="">	
<h4>Estadio Nacional - Brasilia</h4>	
<p>El Estadio Nacional de Brasilia, que sigue los patrones de una de las ciudades arquitectónicamente más modernas del país, será uno de los recintos más imponentes y el segundo más grande (con un aforo de 70.042 espectadores) de la Copa Mundial de la FIFA Brasil 2014™. 
</p>
<img src="img/estadio3.jpg" alt="">	
<h4>Arena Pantanal - Cuiabá</h4>
<p>El estadio que albergará los partidos de la Copa Mundial de la FIFA Brasil 2014™ al borde de una región de flora y fauna tan rica como la del Pantanal, no podía dejar de incluir entre sus objetivos la construcción y el mantenimiento de una estructura sostenible desde su raíz.  </p>

<img src="img/estadio4.jpeg" height="464" width="619" alt="">
<h4>Arena da Baixada - Curitiba</h4>
<p>Desde que fue reinaugurado en junio de 1999, el tradicional estadio Joaquim Américo, conocido como Arena da Baixada, tiene la reputación de ser uno de los más modernos y mejor estructurados de Brasil.  </p>

<img src="img/estadio5.jpg"alt="">
<h4>Estadio Castelão - Fortaleza</h4>
<p>Es habitual ver a los hinchas de Fortaleza demostrar su pasión por el fútbol y por sus dos principales equipos, Ceará y Fortaleza. Y pronto tendrán un nuevo recinto en el que disfrutar tanto de los clásicos que suelen paralizar la ciudad como de los partidos de la Copa FIFA Confederaciones 2013 y la Copa Mundial de la FIFA 2014™.  </p>

<img src="img/estadio6.jpg"alt="">
<h4>Arena Amazônia - Manaos</h4>
<p>Puede que el Arena Amazonia, antiguo Estadio Vivaldão, no sea un escenario tradicional del fútbol brasileño, pero sin duda atraerá a un gran número de aficionados gracias a su ubicación privilegiada, en el corazón de la selva más extensa del mundo. </p>

