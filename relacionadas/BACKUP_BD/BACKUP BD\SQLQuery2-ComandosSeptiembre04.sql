FROM AUTOR
WHERE NOMBRE LIKE 'M%'

-- LISTAR NOMBRES DE AUTORES CUYO NOMBRE TERMINE EN O. 
SELECT NOMBRE,EDAD
FROM AUTOR
WHERE NOMBRE LIKE '%O'

-- LISTAR NOMBRES DE AUTORES CUYO NOMBRE CONTENGA LA LETRA S.
SELECT NOMBRE,EDAD
FROM AUTOR
WHERE NOMBRE LIKE '%S%'

-- NUEVO TEMA... CUALIFICACION DE ATRIBUTOS

--1. SIN RENOMBRAR TABLA
-- LISTAR TITULO, NUMERO DE PAGINAS Y PRECIO DE LOS LIBROS
-- QUE TENGAN MAS DE 100 UNIDADES EN EXISTENCIA

SELECT TITULO,NPAGS,PRECIO
FROM LIBRO
WHERE existencia > 100

-- CUALIFIQUE ATRIBUTOS SIN RENOMBRAR LA TABLA

SELECT LIBRO.TITULO,LIBRO.NPAGS,LIBRO.PRECIO
FROM LIBRO
WHERE LIBRO.existencia > 100

--2. RENOMBRANDO TABLA

SELECT L.TITULO,L.NPAGS,L.PRECIO
FROM LIBRO L                      --L NUEVO NOMBRE DE LA TABLA
WHERE L.existencia > 100

-- CUALIFICAR ANTEPONER LA TABLA A CADA ATRIBUTO
-- CUALIFICAR CUANDO ES MAS DE DOS TABLAS Y RELACIONARLAS 

SELECT L.TITULO, L.NPAGS, L.PRECIO,L.CODIGO
FROM LIBRO L,GENERO G
WHERE L.EXISTENCIA > 100


