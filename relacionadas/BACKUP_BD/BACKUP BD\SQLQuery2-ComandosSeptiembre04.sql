-- SIGUIENTE TEMA.....
-- LISTAR EL TITUO Y PRECIO DE CADA UNO DE LOS LIBROS
-- DICHA CONSULTA DEBE SALIR ORDENADA DE TAL MANERA QUE
-- EL LIBRO MAS CARO SALGA DE PRIMERO Y EL MAS BARATO DE ULTIMO


SELECT L.TITULO,L.PRECIO
FROM LIBRO L
ORDER BY L.PRECIO DESC         --  DESC (MAS ALTO AL MAS BAJO) OR  ASC (MAS BAJO AL MAS ALTO)


-- LISTAR TITULO,PRECIO, Y NUMERO DE PAGINAS DE LOS LIBROS
-- QUE TENGAN MENOS DE 200 PAGINAS. DICHA CONSULTA DEBE
-- SALIR ORDENADA ALFABETICAMENTE POR TITULO DE LIBRO

SELECT L.TITULO, L.PRECIO, L.NPAGS
FROM LIBRO L
WHERE L.NPAGS < 200
ORDER BY L.TITULO ASC       -- ASC == ALFABETICAMENTE   NO ES OBLIGATORIO EL ASC PARA ASENDENTEMENTE

